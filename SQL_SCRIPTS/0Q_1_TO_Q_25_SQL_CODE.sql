-- Q1. How many total orders has UrbanCart received so far?
SELECT COUNT(DISTINCT order_id) AS "Total_Orders"
FROM public.FactOrders;


-- Q2. Which cities generate the highest number of orders & revenue?
WITH Order_Revenue AS (
    SELECT 
        o.order_id,
        o.customer_id,
        SUM(oi.quantity * p.unit_price) AS order_total
    FROM public.FactOrders o
    JOIN public.FactOrderItems oi ON o.order_id = oi.order_id
    JOIN public.DimProducts p ON oi.product_id = p.product_id
    GROUP BY o.order_id, o.customer_id
)
SELECT 
    c.city AS "City",
    COUNT(DISTINCT r.order_id) AS "Total_Orders",
    ROUND(SUM(r.order_total), 2) AS "Total_Revenue"
FROM public.DimCustomers c
JOIN Order_Revenue r ON c.customer_id = r.customer_id
GROUP BY c.city
ORDER BY "Total_Revenue" DESC;


-- Q3. What percent of total customers uses gmail?
WITH Gmail_Count AS (
    SELECT COUNT(*) AS Gmail_Users 
    FROM public.DimCustomers 
    WHERE email LIKE '%@gmail.com'
),
Total_Count AS (
    SELECT COUNT(*) AS Total_Customers 
    FROM public.DimCustomers
)
SELECT 
    g.Gmail_Users AS "Total_Gmail_User",
    t.Total_Customers AS "Total_Customers", 
    ROUND((g.Gmail_Users * 100.0) / t.Total_Customers, 2) AS "Gmail_Percentage(%)"
FROM Gmail_Count g, Total_Count t;


-- Q4. What is the monthly trend of total orders over time?
SELECT 
    DATE_TRUNC('month', order_date) AS "Month", 
    COUNT(DISTINCT order_id) AS "Total_Orders"
FROM public.FactOrders
GROUP BY 1
ORDER BY 1 ASC;


-- Q6. What is the total revenue generated by UrbanCart?
SELECT ROUND(SUM(oi.quantity * p.unit_price), 2) AS "Total_Revenue"
FROM public.FactOrderItems oi
JOIN public.DimProducts p ON oi.product_id = p.product_id;


-- Q7. Which product categories contribute the most to total revenue?
SELECT 
    p.category AS "Category_Name", 
    ROUND(SUM(oi.quantity * p.unit_price), 2) AS "Total_Revenue"
FROM public.FactOrderItems oi
JOIN public.DimProducts p ON oi.product_id = p.product_id
GROUP BY 1
ORDER BY 2 DESC;


-- Q8. Which individual products generate the highest revenue?
SELECT 
    p.product_name AS "Product_Name", 
    ROUND(SUM(oi.quantity * p.unit_price), 2) AS "Total_Revenue"
FROM public.FactOrderItems oi
JOIN public.DimProducts p ON oi.product_id = p.product_id
GROUP BY p.product_name
ORDER BY "Total_Revenue" DESC
LIMIT 10;


-- Q9. What is the average order value (AOV) and Average Basket Size?
WITH Order_Metrics AS (
    SELECT 
        oi.order_id,
        SUM(oi.quantity) AS total_items,
        SUM(oi.quantity * p.unit_price) AS order_value
    FROM public.FactOrderItems oi
    JOIN public.DimProducts p ON oi.product_id = p.product_id
    GROUP BY oi.order_id
)
SELECT 
    ROUND(AVG(order_value), 2) AS "Average_Order_Value",
    ROUND(AVG(total_items), 2) AS "Average_Basket_Size"
FROM Order_Metrics;


-- Q10. Which products are at risk of stock-out due to high sales volume and low inventory?
SELECT 
    p.product_name AS "Product_Name",
    p.stock AS "Current_Stock",
    SUM(oi.quantity) AS "Sales_Volume"
FROM public.FactOrderItems oi
JOIN public.DimProducts p ON oi.product_id = p.product_id
JOIN public.FactOrders o ON oi.order_id = o.order_id
WHERE o.order_date >= DATE '2025-11-01'  -- আপনার ডাটার টাইমলাইন অনুযায়ী ফিক্সড ডেট
GROUP BY p.product_name, p.stock
HAVING p.stock < SUM(oi.quantity)
ORDER BY "Current_Stock" ASC, "Sales_Volume" DESC;


-- Q11. Which customers contribute the highest total revenue?
WITH Customer_Spend AS (
    SELECT 
        o.customer_id,
        SUM(oi.quantity * p.unit_price) AS total_spent
    FROM public.FactOrders o
    JOIN public.FactOrderItems oi ON o.order_id = oi.order_id
    JOIN public.DimProducts p ON oi.product_id = p.product_id
    GROUP BY o.customer_id
)
SELECT 
    c.customer_id,
    c.full_name,
    ROUND(cs.total_spent, 2) AS "Total_Spent"
FROM Customer_Spend cs
JOIN public.DimCustomers c ON c.customer_id = cs.customer_id
ORDER BY "Total_Spent" DESC
LIMIT 10;


-- Q12. Generate a list of cancellation rate by city; also the cancellation rate for customers.
-- (Part A: By City)
SELECT 
    c.city AS "City",
    COUNT(DISTINCT o.order_id) AS "Total_Orders",
    COUNT(DISTINCT CASE WHEN o.status = 'Cancelled' THEN o.order_id END) AS "Cancelled_Orders",
    ROUND((COUNT(DISTINCT CASE WHEN o.status = 'Cancelled' THEN o.order_id END) * 100.0) / COUNT(DISTINCT o.order_id), 2) AS "Cancellation_Rate(%)"
FROM public.DimCustomers c
JOIN public.FactOrders o ON c.customer_id = o.customer_id
GROUP BY c.city
ORDER BY "Cancellation_Rate(%)" DESC;

-- (Part B: By Customer)
SELECT 
    c.customer_id,
    c.full_name,
    COUNT(DISTINCT o.order_id) AS "Total_Orders",
    COUNT(DISTINCT CASE WHEN o.status = 'Cancelled' THEN o.order_id END) AS "Cancelled_Orders",
    ROUND((COUNT(DISTINCT CASE WHEN o.status = 'Cancelled' THEN o.order_id END) * 100.0) / NULLIF(COUNT(DISTINCT o.order_id), 0), 2) AS "Cancellation_Rate(%)"
FROM public.DimCustomers c
JOIN public.FactOrders o ON c.customer_id = o.customer_id
GROUP BY c.customer_id, c.full_name
ORDER BY "Cancellation_Rate(%)" DESC;


-- Q13. Do male and female customers show different purchasing patterns by category?
SELECT 
    c.gender AS "Gender",
    p.category AS "Category",
    COUNT(DISTINCT o.order_id) AS "Total_Orders",
    SUM(oi.quantity) AS "Total_Quantity_Purchased",
    ROUND(SUM(oi.quantity * p.unit_price), 2) AS "Total_Revenue",
    ROUND(SUM(oi.quantity)::NUMERIC / COUNT(DISTINCT o.order_id), 2) AS "Avg_Basket_Size_Per_Order",
    ROUND(SUM(oi.quantity * p.unit_price)::NUMERIC / COUNT(DISTINCT o.order_id), 2) AS "Avg_Order_Value_AOV"
FROM public.DimCustomers c
JOIN public.FactOrders o ON c.customer_id = o.customer_id
JOIN public.FactOrderItems oi ON o.order_id = oi.order_id
JOIN public.DimProducts p ON oi.product_id = p.product_id
WHERE c.gender IS NOT NULL 
GROUP BY c.gender, p.category
ORDER BY c.gender ASC, "Total_Revenue" DESC;


-- Q14. How does customer purchasing behavior change over time since account creation?
WITH Customer_Monthly_Order AS (
    SELECT 
        o.customer_id,
        o.order_id,
        o.order_date,
        SUM(oi.quantity * p.unit_price) AS order_revenue
    FROM public.FactOrders o
    JOIN public.FactOrderItems oi ON o.order_id = oi.order_id
    JOIN public.DimProducts p ON oi.product_id = p.product_id
    GROUP BY o.customer_id, o.order_id, o.order_date
)
SELECT
    c.customer_id,
    c.full_name,
    c.created_at AS account_creation_date,
    ((EXTRACT(YEAR FROM m.order_date) - EXTRACT(YEAR FROM c.created_at)) * 12 + 
     (EXTRACT(MONTH FROM m.order_date) - EXTRACT(MONTH FROM c.created_at))) AS month_since_signup,
    COUNT(DISTINCT m.order_id) AS total_orders,
    ROUND(SUM(m.order_revenue), 2) AS total_spent,
    ROUND(AVG(m.order_revenue), 2) AS avg_order_value
FROM public.DimCustomers c
JOIN Customer_Monthly_Order m ON c.customer_id = m.customer_id
GROUP BY
    c.customer_id,
    c.full_name,
    c.created_at,
    ((EXTRACT(YEAR FROM m.order_date) - EXTRACT(YEAR FROM c.created_at)) * 12 + 
     (EXTRACT(MONTH FROM m.order_date) - EXTRACT(MONTH FROM c.created_at)))
ORDER BY c.customer_id, month_since_signup;


-- Q15. Generate a list of customers who ordered in October but didn’t order in December.
