-- Q18. Is there any relationship between payment method and order status?
WITH Method_Status_Counts AS (
    SELECT 
        fp.method AS "Payment_Method",
        fo.status AS "Order_Status",
        COUNT(DISTINCT fo.order_id) AS "Total_Orders"
    FROM public.FactPayment fp
    JOIN public.FactOrders fo ON fp.order_id = fo.order_id
    GROUP BY fp.method, fo.status
),
Method_Totals AS (
    SELECT 
        "Payment_Method",
        SUM("Total_Orders") AS "Method_Total_Orders"
    FROM Method_Status_Counts
    GROUP BY "Payment_Method"
)
SELECT 
    m."Payment_Method",
    m."Order_Status",
    m."Total_Orders",
    ROUND((m."Total_Orders" * 100.0) / t."Method_Total_Orders", 2) AS "Percentage_Within_Method(%)"
FROM Method_Status_Counts m
JOIN Method_Totals t ON m."Payment_Method" = t."Payment_Method"
ORDER BY m."Payment_Method", m."Total_Orders" DESC;


-- Q19. Do certain cities prefer specific payment methods?
SELECT 
    c.city AS "City",
    fp.method AS "Payment_Method",
    COUNT(fp.payment_id) AS "Usage_Count",
    ROW_NUMBER() OVER (PARTITION BY c.city ORDER BY COUNT(fp.payment_id) DESC) AS "Preference_Rank"
FROM public.DimCustomers c
JOIN public.FactOrders fo ON c.customer_id = fo.customer_id
JOIN public.FactPayment fp ON fo.order_id = fp.order_id
GROUP BY c.city, fp.method
ORDER BY c.city, "Usage_Count" DESC;


-- Q20. Are higher-value orders associated with specific payment methods?
WITH Order_Values AS (
    SELECT 
        fo.order_id,
        SUM(oi.quantity * p.unit_price) AS order_total
    FROM public.FactOrders fo
    JOIN public.FactOrderItems oi ON fo.order_id = oi.order_id
    JOIN public.DimProducts p ON oi.product_id = p.product_id
    GROUP BY fo.order_id
)
SELECT 
    fp.method AS "Payment_Method",
    ROUND(AVG(ov.order_total), 2) AS "Average_Order_Value",
    ROUND(MAX(ov.order_total), 2) AS "Max_Order_Value"
FROM public.FactPayment fp
JOIN Order_Values ov ON fp.order_id = ov.order_id
GROUP BY fp.method
ORDER BY "Average_Order_Value" DESC;


-- Q21. What is the average number of items per order by payment method?
WITH Order_Items AS (
    SELECT 
        order_id,
        SUM(quantity) AS total_items
    FROM public.FactOrderItems
    GROUP BY order_id
)
SELECT 
    fp.method AS "Payment_Method",
    ROUND(AVG(oi.total_items), 2) AS "Average_Items_Per_Order"
