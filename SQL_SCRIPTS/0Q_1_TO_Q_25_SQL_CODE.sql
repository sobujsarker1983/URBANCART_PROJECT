-- Q23. Which product pairs are most frequently purchased together in the same order?
WITH Order_Product_List AS (
    SELECT order_id, product_id 
    FROM public.FactOrderItems
    GROUP BY order_id, product_id
)
SELECT 
    p1.product_name AS "Product_1",
    p2.product_name AS "Product_2",
    COUNT(DISTINCT opl1.order_id) AS "Times_Purchased_Together"
FROM Order_Product_List opl1
JOIN Order_Product_List opl2 ON opl1.order_id = opl2.order_id AND opl1.product_id < opl2.product_id
JOIN public.DimProducts p1 ON opl1.product_id = p1.product_id
JOIN public.DimProducts p2 ON opl2.product_id = p2.product_id
GROUP BY p1.product_name, p2.product_name
ORDER BY "Times_Purchased_Together" DESC
LIMIT 10;


-- Q24. Which product pairs generate the highest total revenue when sold together?
WITH Order_Product_Revenue AS (
    SELECT 
        oi.order_id, 
        oi.product_id,
        SUM(oi.quantity * p.unit_price) AS product_revenue
    FROM public.FactOrderItems oi
    JOIN public.DimProducts p ON oi.product_id = p.product_id
    GROUP BY oi.order_id, oi.product_id
)
SELECT 
    p1.product_name AS "Product_1",
    p2.product_name AS "Product_2",
    COUNT(DISTINCT opr1.order_id) AS "Times_Purchased_Together",
    ROUND(SUM(opr1.product_revenue + opr2.product_revenue), 2) AS "Combined_Revenue"
FROM Order_Product_Revenue opr1
JOIN Order_Product_Revenue opr2 ON opr1.order_id = opr2.order_id AND opr1.product_id < opr2.product_id
JOIN public.DimProducts p1 ON opr1.product_id = p1.product_id
JOIN public.DimProducts p2 ON opr2.product_id = p2.product_id
GROUP BY p1.product_name, p2.product_name
ORDER BY "Combined_Revenue" DESC
LIMIT 10;


-- Q25. Generate a daily report containing the columns Date, Total Orders, Total Items, Completed Orders, Cancelled Orders, Total Revenue 
SELECT
    DATE(o.order_date) AS "Date",
    COUNT(DISTINCT o.order_id) AS "Total_Orders",
    SUM(oi.quantity) AS "Total_Items",
    COUNT(DISTINCT CASE WHEN o.status = 'Delivered' THEN o.order_id END) AS "Completed Orders",
    COUNT(DISTINCT CASE WHEN o.status = 'Cancelled' THEN o.order_id END) AS "Cancelled Orders",
    ROUND(SUM(CASE WHEN o.status = 'Delivered' THEN oi.quantity * p.unit_price ELSE 0 END), 2) AS "Total_Revenue"
FROM public.FactOrders o
JOIN public.FactOrderItems oi ON o.order_id = oi.order_id
JOIN public.DimProducts p ON oi.product_id = p.product_id
GROUP BY DATE(o.order_date)
ORDER BY "Date" DESC;